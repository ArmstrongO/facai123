# 规范
# 硬性规则
# 1. 起变量名和文件名的时候，不能以数字开头。
# 1a = 10
# 2. 起变量名的时候不能与标识符同名  标识符（关键字）
# break1 = 10

# 建议性规则
# 1. 起变量名的时候必须有见名知意的效果。
# student = "小孙"
# 百度查出来英文单词，然后定义成变量名，旁边写上注释。
# price = 500
# age = 18

# 2. 当一个变量名没有办法用一个英文单词描述的时候，可以用多个单词进行拼接。
# python最推荐的拼接方式  _
# 学生姓名
student_name = "小孙"
# 商城价格
mall_price = 200
# 收货地址详情
receive_address_detail = "上海浦东"
